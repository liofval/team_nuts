export function applyTemplate(editor: Editor, templateIndex: number) {
  const template = TEMPLATES[templateIndex];
  const html = template.content
    .split("\n\n")
    .map((block) => `<p>${block.replace(/\n/g, "<br>")}</p>`)
    .join("");
  editor.commands.setContent(html);
}
